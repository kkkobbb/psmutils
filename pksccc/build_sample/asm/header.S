# タイトルで日本語を使用する場合、
# このファイルをsjisで保存する必要がある

	.section scheader
	.code 32

 	.globl header_title
header_title:
	.ascii	"SC"
	# 7-4bit: 1, 3-0bit: ICON number
	.byte	0x11
	.byte	__block_num

	# タイトルは64byte
	# 本来は1文字16bitのShift-JISを使用して32文字まで (00h埋め)
	# pocketstationではアルファベットでも2byteの割り当てがあり、そちらを使うべきみたい
	# ただし、1byte(ascii)も使用できる
	#       "<----------- 32byte ----------->"
	.ascii	"TEST PROGRAM                    "
	.ascii	"                                "

	.space	12

	# pocketstation file header
	.byte	1,0     /* Number of file viewr mono icon frames */
	.ascii	"MCX0"  /* Pocketstation identifier */
	.byte	1       /* Number of entries in executable mono icon list */
	.byte	0       /* Number of BU command 5Bh/5Ch Get/Set Functions */
	.word	0       /* Reserved (zero) */

	# entrypoint
	.word	_start

	# アイコン
	.globl header_icon
